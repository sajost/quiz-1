$.fn.center = function () {
	var w = w || $(window);
	this.css({
		'position':'absolute',
		'top':Math.abs(((w.height() - this.outerHeight()) / 2) + w.scrollTop()),
		'left':Math.abs(((w.width() - this.outerWidth()) / 2) + w.scrollLeft())
	});
	return this;
}

